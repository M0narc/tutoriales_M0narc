name = "Bro"

# lengt
print(len(name))

# find something
print(name.find("o"))

# CAPITALIZE
print(name.capitalize())

# ALL UPPER
print(name.upper())

# ALL LOWER
print(name.lower())

# BOOLEAN digits
print(name.isdigit())

# BOOLEAN numbers
print(name.isalpha())

# "word counter"
print(name.count("o"))

# REPLACE
print(name.replace("o", "a"))

# multiply
print(name * 3)
